const API_BASE = '/api';

export async function fetchMarketStatus(): Promise<MarketStatus> {
  try {
    const res = await fetch(`${API_BASE}/market/status`);
    if (!res.ok) throw new Error('API offline');
    const json = await res.json();
    return json.data;
  } catch {
    return mockMarketStatus();
  }
}

