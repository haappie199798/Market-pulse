export async function fetchNews(category: string = 'ALL', tag: string = ''): Promise<NewsArticle[]> {
  try {
    const res = await fetch(`${API_BASE}/news?category=${category}&tag=${tag}`);
    if (!res.ok) throw new Error('API offline');
    const json = await res.json();
    return json.data;
  } catch {
    let filtered = [...SAMPLE_NEWS];
    if (category !== 'ALL') {
      filtered = filtered.filter(n => n.category === category);
    }
    if (tag) {
      filtered = filtered.filter(n => n.tags.includes(tag));
    }
    return filtered;
  }
}

